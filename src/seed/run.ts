/* ---- Lexical helpers ---- */
const t = (text: string, format = 0) => ({
  type: 'text', text, format, style: '', mode: 'normal', detail: 0, version: 1,
})
const h = (tag: 'h2' | 'h3', text: string) => ({
  type: 'heading', tag, version: 1, format: '', indent: 0, direction: 'ltr', children: [t(text)],
})
const p = (text: string) => ({
  type: 'paragraph', version: 1, format: '', indent: 0, direction: 'ltr', textFormat: 0,
  children: text ? [t(text)] : [],
})
const list = (items: string[], ordered = false) => ({
  type: 'list', listType: ordered ? 'number' : 'bullet', tag: ordered ? 'ol' : 'ul',
  start: 1, version: 1, format: '', indent: 0, direction: 'ltr',
  children: items.map((it, i) => ({
    type: 'listitem', value: i + 1, version: 1, format: '', indent: 0, direction: 'ltr',
    children: [t(it)],
  })),
})
const block = (blockType: string, fields: Record<string, unknown>) => ({
  type: 'block', version: 2, format: '',
  fields: { id: randomUUID(), blockType, ...fields },
})
const doc = (children: unknown[]) => ({
  root: { type: 'root', format: '', indent: 0, version: 1, direction: 'ltr', children },
})
const rows = (arr: Record<string, unknown>[]) => arr.map((r) => ({ id: randomUUID(), ...r }))
const txtRows = (arr: string[]) => rows(arr.map((text) => ({ text })))

type ScreenDef = {
  num?: string
  name: string
  was?: string[]
  now?: string[]
  note?: string
  images?: (number | string)[]
}
const screen = (s: ScreenDef) =>
  block('screen', {
    num: s.num,
    name: s.name,
    was: s.was ? txtRows(s.was) : [],
    now: s.now ? txtRows(s.now) : [],
    note: s.note,
    images: rows((s.images ?? []).map((image) => ({ image }))),
  })

